def shorten(s: str):
    return s if len(s) <= 50 else s[:50] + "..."


if __name__ == "__main__":
    root_path = Path(os.path.dirname(__file__))
    config_path = root_path / 'github.config.json'

    with config_path.open() as config_file:
        config = json.load(config_file)

    login = config['login']
    token = config['token']
    organization = config['organization']
    username = config['username']
    host = config['github_host']

    prs = requests.get(f"{host}/api/v3/search/issues?q=is%3Aopen+is%3Apr+org%3A{organization}+archived%3Afalse",
                       auth=(login, token)).json()
    pr_count = prs['total_count']

    issues = requests.get(f"{host}/api/v3/search/issues?q=is%3Aopen+is%3Aissue+assignee%3A{username}+archived%3Afalse",
                          auth=(login, token)).json()
    issues_count = issues['total_count']

    issues_count_string = f"(assigned issues: {issues_count})" if issues_count > 0 else ""

    message = f'{pr_count} {issues_count_string}| image={GH_ICON} imageWidth=18'
    print(message)
    print("---")

    if pr_count > 0:
        print("<b>Open Pull Requests:</b>")
        for pr in prs.get('items', []):
            title = shorten(pr['title'])
            user = pr['user']['login']
            repository = pr['repository_url'].split('/')[-1]

            pr_details_url = pr.get('pull_request', {}).get('url')
            if pr_details_url:
                pr_details = requests.get(pr_details_url,
                                          auth=(login, token)).json()
                statuses_url = pr_details['_links'].get('statuses', {}).get(
                    'href')
                statuses = requests.get(statuses_url,
                                        auth=(login, token)).json()
                status_icon = '‚ùå' if any([True for s in statuses if
                                          s['state'] == 'failure']) else '‚úîÔ∏è'

            print(f"{title} \r<small>{status_icon} <b>üì¶ {repository}</b> üë§ {user}</small>| href={pr['html_url']} ")

        print("---")

        print(f"üîç All pull request of {organization} | href={host}/pulls?utf8=%E2%9C%93&q=is%3Aopen+is%3Apr+org%3A{organization}")
        print("---")

    if issues_count > 0:
        print("<b>Issues assigned to me:</b>")

        for pr in issues.get('items', []):
            title = shorten(pr['title'])
            user = pr['user']['login']
            repository = pr['repository_url'].split('/')[-1]
            print(f"{title} | href={pr['html_url']} ")

        print("---")
    for links in config.get("links", []):
        print(f"{links['title']} | href={links['url']}")
